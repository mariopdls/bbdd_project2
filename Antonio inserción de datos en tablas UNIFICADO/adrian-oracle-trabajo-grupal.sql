COD_POSTAL VARCHAR2(20),

CONSTRAINT PK_COD_POSTALES PRIMARY KEY (COD_POSTAL)
);
CREATE TABLE ESTACION( 
COD VARCHAR2(20),
COD_POSTAL VARCHAR2(20),

