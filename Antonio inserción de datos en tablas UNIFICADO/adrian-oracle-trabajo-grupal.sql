CREATE TABLE ATENCION_AL_CLIENTE ( 
DNI VARCHAR2(20),
PUESTO VARCHAR2(20),

CONSTRAINT PK_ATENCION_AL_CLIENTE  PRIMARY KEY (DNI),
CONSTRAINT FK_ATENCION_AL_CLIENTE_PERSONAL FOREIGN KEY (DNI) REFERENCES PERSONAL (DNI)
