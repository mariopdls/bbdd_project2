COD_ESTACION VARCHAR2(20),

CONSTRAINT PK_PERSONAL PRIMARY KEY (DNI),
CONSTRAINT FK_PERSONAL_ESTACION FOREIGN KEY (COD_ESTACION) REFERENCES ESTACION (COD)
